# შექმენით ფუნქცია, რომელსაც გადაეცემა წინადადება. თქვენი დავალებაა ამ წინადადების სიტყვები შეაბრუნოთ
#  და დააბრუნოთ(სიტყვების სიმბოლოები არ უნდა იყოს შებრუნებული)

def turned_words(text):
    # წინადადების სიტყვებად დაყოფა
    words = text.split()
    
    # სიტყვების შებრუნება
    reversed_words = words[::-1]
    
    # შებრუნებული სიტყვების დაკავშირება space-ით
    return " ".join(reversed_words)





